// require 路径不使用相对路径(不加 './')时，自动在node_modules里面找
const test1 = require('test1.js')

// require 路径不使用相对路径(不加 './')，且不加后缀 '.js' 时，自动在node_modules里面的当前目录里面找index.js(因为index.js默认为入口文件)
const test2 = require('test2')

// require 路径不使用相对路径(不加 './')，且不加后缀 '.js'，且没有index.js默认入口文件时，可以在package.json文件中使用main字段指定入口文件名称
const test3 = require('test3')

console.log(test1.msg)
console.log(test2.msg)
console.log(test3.msg)
